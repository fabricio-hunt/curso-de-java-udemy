public class Casting {
    public static void main(String[] args) {
        int a , b;
        double resultado;

        a = 5;
        b = 2;
        // Processo de Casting <variável> = <casting> <expressão>
        resultado = (double) a / b;


        System.out.println("O resultado após o casting é: " + resultado);
    }
}
