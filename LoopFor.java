public class LoopFor {
    public static void main(String[] args) {
        /* for (inicialização; condição; incremento/decremento) {
         Bloco de código a ser executado}*/

        for (int i = 1; i < 11; i++) {
            System.out.println("Número: " + i);
        }

            /*Componentes do Loop for
            Inicialização:

            É executada apenas uma vez, antes do início do loop.
            Geralmente usada para declarar e inicializar uma variável de controle.
            Exemplo: int i = 0.

            Condição:

            É avaliada antes de cada iteração do loop.
            Enquanto a condição for true, o bloco de código será executado.
            Exemplo: i < 10.
            Incremento/Decremento:

            Executado ao final de cada iteração.
            Geralmente usado para atualizar a variável de controle.
            Exemplo: i++ (incremento de 1 em 1) ou i-- (decremento de 1 em 1).*/


                }
            }
